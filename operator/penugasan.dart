void main() {
  // operator =
  dynamic a = 10;
  print(a);
  // operator +=
  a += 5; // sama dengan a = a + 5
  print(a);
  // operator -=
  a -= 3; // sama dengan a = a - 3
  print(a);
  // operator *=
  a *= 2; // sama dengan a = a * 2
  print(a);
  // operator /=
  a /= 4; // sama dengan a = a / 4
  print(a);
  // operator ~=
  a ~/= 2; // sama dengan a = a ~/ 2 (pembagian bulat)
  print(a);
}
